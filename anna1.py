
number = str(input("input a number from 0 to 6 and see which day it is\n"))
if number == "0":
    print("Sunday")
    
elif number == "1":
    print("Monday")
    
elif number == "2":
    print("Tuesday")
    
elif number == "3":
    print("Wednesday")

elif number == "4":
    print("Thursday")

elif number == "5":
    print("Friday")

elif number == "6":
    print("Saturday")

else:
    print("please input a number from 0 to 6, other numbers does not work here")     
    